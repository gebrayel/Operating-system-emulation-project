package Classes;

public class Checked {

    private Node node;
    private int action;

    public Checked(Node node, int action) {
        this.node = node;
        this.action = action;
    }

    public Node getNode() {
        return node;
    }

    public void setNode(Node node) {
        this.node = node;
    }

    public int getAction() {
        return action;
    }

    public void setAction(int action) {
        this.action = action;
    }

}
